function multiplyBy()
{
        firstNumber = document.getElementById("firstNumber").value;
        secondNumber = document.getElementById("secondNumber").value;
        document.getElementById("result").innerHTML = firstNumber * secondNumber;
}

function divideBy() 
{ 
        firstNumber = document.getElementById("firstNumber").value;
        secondNumber = document.getElementById("secondNumber").value;
document.getElementById("result").innerHTML = firstNumber / secondNumber;
}
